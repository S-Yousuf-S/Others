def render_notebook_content(content):
    """
    Renders a notebook's extracted text in the Streamlit UI.
    Handles the special case of inline base64 images that PyPDF2/nbformat
    may embed directly in the source — these are stripped from the text
    and rendered separately as st.image() calls below the text.
    """
    # Regex pattern to detect embedded base64 images in markdown image syntax:
    # ![alt text](data:image/png;base64,<very long string>)
    pattern = r'!\[.*?\]\((data:image\/.*?;base64,.*?)\)'

    # Capture all embedded image data URIs before removing them from text
    matches = re.findall(pattern, content)

    # Replace inline base64 blobs with a human-readable placeholder.
    # Leaving them in would make the markdown output unreadable.
    cleaned_text = re.sub(pattern, '*[Image omitted from text]*', content)

    # Collapse any excessive blank lines introduced by the substitution
    cleaned_text = re.sub(r'\n{3,}', '\n\n', cleaned_text)

    # Render the cleaned markdown text (includes fenced code blocks)
    st.markdown(cleaned_text)

    # Render any extracted images below the text as actual image widgets
    for image_data in matches:
        try:
            header, encoded = image_data.split(",", 1)
            image_bytes = base64.b64decode(encoded)
            st.image(image_bytes)
        except:
            # If decoding fails for any reason, silently skip — don't crash
            pass


