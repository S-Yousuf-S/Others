# 🧹 MARKDOWN NORMALIZER
# -------------------------------
def normalize_markdown(text):
    """
    Cleans raw extracted text before displaying it in the UI.
    Handles common issues from PDF extraction: broken line endings,
    excessive blank lines, and malformed heading syntax.
    """
    # Standardise Windows-style line endings (\r\n) to Unix (\n)
    text = text.replace("\r\n", "\n")

    # PDFs often wrap long sentences mid-word at page column boundaries,
    # inserting a single \n that isn't a real paragraph break. This regex
    # removes those soft line breaks while preserving intentional paragraph
    # separations (which use double \n\n).
    text = re.sub(r'(?<!\n)\n(?!\n)', ' ', text)

    # Collapse 3 or more consecutive blank lines down to 2.
    # Keeps visual spacing clean without excessive gaps.
    text = re.sub(r"\n{3,}", "\n\n", text)

    # Ensure markdown headings always have a space after the # symbol.
    # Without this, "#Heading" won't render as a heading — it stays as raw text.
    text = re.sub(r'(?m)^#([^\s])', r'# \1', text)

    return text.strip()


