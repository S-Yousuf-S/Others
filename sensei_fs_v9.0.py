#   2. At least one document has been uploaded (documents is not empty)
#   3. The API key is connected and valid
if question and documents and st.session_state.api_configured:

    # Render the user's message in the chat
    with st.chat_message("user"):
        st.markdown(question)

    # Run the retrieval engine to find relevant source chunks
    relevant_sources = find_relevant_sources(question, documents)

    answers = []  # Will hold (source_type, retrieved_text) pairs for saving

    with st.chat_message("assistant"):

        if not relevant_sources:
            # No matching chunks found — inform the user clearly
            summary = "❌ Not found in uploaded material"
            st.markdown(summary)
        else:
            # Send retrieved context + question to Gemini and get explanation.
            # Spinner wraps only the API call — not the expander rendering below.
            with st.spinner("Sensei is thinking..."):
                summary = generate_summary(question, relevant_sources)

            # Display the AI-generated explanation first
            st.markdown("📌 **Main Explanation**")
            st.markdown(summary)
            st.markdown("---")

            # Then display the raw source evidence in colour-coded expanders
            for src, text in relevant_sources:
                response = text
                answers.append((src, response))  # Save for chat history

                label = ""
                if src == "pdf":
                    label = "🟥 PDF Source — Retrieved Passages"
                elif src == "notebook":
                    label = "🟩 Notebook Source — Code & Notes"
                elif src == "slides":
                    label = "🟦 Slides Source — Key Points"

