def sanitize_text(text):
    """Removes illegal surrogate characters that crash Streamlit rendering."""
    # This regex strips out the corrupted unicode ranges (U+D800 to U+DFFF)
    return re.sub(r'[\ud800-\udfff]', '', text)

# Each reader receives a Streamlit UploadedFile object and returns
# a single plain-text string representing the document's content.
# That string is stored in the documents dict and later chunked
# by the retrieval engine.

def read_pdf(file):
    # PdfReader opens the PDF and gives access to each page.
    # extract_text() can return None on image-only pages, so
    # we use `or ""` to safely fall back to an empty string.
    reader = PdfReader(file)
    # Changed "" to "\n\n" to ensure pages are split into separate chunks!
    full_text = "\n\n".join([p.extract_text() or "" for p in reader.pages])
    return sanitize_text(full_text)


def read_ipynb(file):
    # Jupyter notebooks are JSON files containing a list of cells.
    # Each cell has a type ("code" or "markdown") and a source list
    # of strings which we join into one string per cell.
    data = json.load(file)
    formatted_content = []

    for cell in data["cells"]:
        cell_type = cell.get("cell_type", "")
        source = "".join(cell.get("source", ""))

        if cell_type == "code":
            # Wrap code cells in fenced Python blocks so Streamlit
            # renders them with syntax highlighting in the expander.
            formatted_content.append(f"```python\n{source}\n```")
        else:
            # Markdown cells (explanations, headings, notes) are kept
            # as-is since st.markdown() will render them correctly.
            formatted_content.append(source)

    # Separate each cell with a blank line so the chunker
    # can split on \n\n later during retrieval.
    full_text = "\n\n".join(formatted_content)
    return sanitize_text(full_text)


def read_pptx(file):
    # python-pptx gives access to each slide and its shapes.
    # A "shape" can be a text box, title, or content placeholder.
    # We extract text from every shape that has non-empty text.
    prs = Presentation(file)
    slides_text = []

    for i, slide in enumerate(prs.slides):
        slide_content = []
        for shape in slide.shapes:
            if hasattr(shape, "text") and shape.text.strip():
                slide_content.append(shape.text.strip())

        if slide_content:
            # The slide label [Slide N] is embedded as the FIRST LINE
            # of the chunk body — not as a separate paragraph.
            # This is critical: a standalone heading paragraph would
            # become its own scoreable chunk in the retrieval engine,
            # causing the wrong slide's content to be returned.
            body = "\n\n".join(slide_content)
            slides_text.append(f"[Slide {i+1}]\n{body}")

    # Slides are separated by a distinctive --- marker.
    # The retrieval engine splits on this marker first, so each slide
    # becomes one atomic chunk that is never split mid-slide.
    full_text = "\n\n---\n\n".join(slides_text)
    return sanitize_text(full_text)


