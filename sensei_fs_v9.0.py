# 🧹 CHUNK FORMATTER FOR EXPANDERS
# -------------------------------
def format_chunks_for_display(raw_text, source_type):
    """
    Prepares retrieved source text for display inside the expander UI.
    Deduplicates, splits into readable paragraphs, and filters out noise.

    Args:
        raw_text    : The combined retrieved chunk string from the engine.
        source_type : "pdf", "notebook", or "slides" — reserved for future
                      source-specific formatting rules.
    Returns:
        List of clean paragraph strings, each rendered as a separate block.
    """
    # Remove duplicate lines (common in PDFs with repeated headers/footers)
    cleaned = clean_retrieved_content(raw_text)

    # Split the cleaned text into paragraph-level blocks using double newlines
    chunks = [c.strip() for c in cleaned.split("\n\n") if c.strip()]

    # Filter out noise: very short fragments (< 30 chars) that aren't headings.
    # These are usually stray punctuation, page numbers, or extraction artefacts.
    chunks = [
        c for c in chunks
        if len(c) >= 30 or c.startswith("#")
    ]

    return chunks


