# 🔍 RETRIEVAL ENGINE
# -------------------------------
def find_relevant_sources(question, documents):
    """
    Searches all uploaded documents for chunks most relevant to the question.

    Algorithm:
      1. Extract meaningful keywords from the question (stop words removed).
      2. For each document source, split text into chunks.
      3. Score each chunk by counting how many keywords it contains.
      4. Expand top chunks with neighbouring context (PDF/notebook only).
      5. Return top-3 chunks per source, deduplicated.

    Args:
        question  : The user's raw question string.
        documents : Dict mapping source type ("pdf", "notebook", "slides")
                    to the full extracted text string.
    Returns:
        List of (source, combined_text) tuples for the most relevant excerpts.
    """
    results = []

    # Common English words that carry no topical meaning.
    # Removing these ensures only meaningful content words are matched.
    stop_words = {
        "what", "is", "are", "the", "how", "and",
        "of", "to", "in", "for", "a", "an",
        "can", "you", "explain", "with", "which", "tell",
        "me", "summarise", "write", "about", "from",
        "list", "give", "define", "does"
    }

    # \b\w+\b extracts whole words only, ignoring surrounding punctuation.
    # e.g. "what's" → ["what", "s"], "AI-powered" → ["AI", "powered"]
    raw_words = re.findall(r'\b\w+\b', question.lower())
    keywords = [word for word in raw_words if word not in stop_words]

    for source, text in documents.items():

        # Split on the --- slide separator first (PPTX), then on double
        # newlines (PDF/notebook). This ensures each slide stays as one
        # atomic unit and never gets split mid-content.
        raw_chunks = re.split(r'\n\n---\n\n|\n\n', text)
        chunks = [c.strip() for c in raw_chunks if c.strip()]

        scored_chunks = []

        for chunk_index, chunk in enumerate(chunks):

            chunk_lower = chunk.lower()

            # Very long chunks (> 1500 chars) are truncated rather than skipped.
            # Skipping would silently drop relevant content from large PDF pages.
            if len(chunk) > 1500:
                chunk = chunk[:1500]
                chunk_lower = chunk.lower()

            # Count how many question keywords appear in this chunk.
            # Higher score = more topically relevant to the question.
            score = sum(1 for keyword in keywords if keyword in chunk_lower)

            if score >= 1:  # At least one keyword must match to be considered

                if source == "slides":
                    # Slides are self-contained units. Expanding into neighbouring
                    # slides would pull in completely unrelated topics, so we
                    # return the matched slide chunk exactly as-is.
                    expanded_chunk = chunk
                else:
                    # For PDF and notebook, surrounding paragraphs often provide
                    # helpful context (e.g. a definition followed by an example).
                    # We grab the chunk plus one paragraph before and after it.
                    context_window = chunks[
                        max(0, chunk_index - 1):
                        min(len(chunks), chunk_index + 2)
                    ]
                    expanded_chunk = "\n\n".join(context_window)

                scored_chunks.append((score, expanded_chunk.strip()))

        # Sort chunks by relevance score descending — highest match first
        scored_chunks.sort(key=lambda x: x[0], reverse=True)

        # Take the top 3 most relevant chunks for this source
        top_chunks = [chunk for score, chunk in scored_chunks[:3]]

        # dict.fromkeys preserves insertion order while removing exact duplicates.
        # This handles the case where context window expansion causes two adjacent
        # chunks to produce identical expanded strings.
        top_chunks = list(dict.fromkeys(top_chunks))

        if top_chunks:
            # Combine the top chunks into one string for this source,
            # separated by blank lines to preserve readability.
            combined = "\n\n".join(top_chunks)
            results.append((source, combined))

    return results


