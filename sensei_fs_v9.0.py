# -------------------------------
# 📄 PROCESS FILES
# -------------------------------
# Every time the page reruns (which Streamlit does on any interaction),
# uploaded_files may or may not be present. We always start with an
# empty dict and rebuild from whatever the user has uploaded this run.
documents = {}

if uploaded_files:
    for file in uploaded_files:
        # Each file type goes to a different key in the documents dict.
        # Multiple files of the same type are accumulated with +=
        # so uploading two PDFs merges both into documents["pdf"].
        if file.name.endswith(".pdf"):
            documents["pdf"] = documents.get("pdf", "") + read_pdf(file)
        elif file.name.endswith(".ipynb"):
            documents["notebook"] = documents.get("notebook", "") + "\n\n" + read_ipynb(file)
        elif file.name.endswith(".pptx"):
            documents["slides"] = documents.get("slides", "") + "\n\n" + read_pptx(file)

    # Serialise to JSON string first so we can check its size before writing.
    docs_payload = json.dumps(documents)

    if len(docs_payload) > 10_000_000:
        # 10MB guard — warn the user rather than silently writing a huge file
        # that could fail on disk-quota-limited deployments (e.g. Streamlit Cloud).
        st.sidebar.warning(
            "⚠️ Uploaded content exceeds 10MB. "
            "Some documents may not be saved. Try uploading fewer or smaller files."
        )
    else:
        # Write the documents dict to the session-specific JSON file.
        # This persists documents across page refreshes within the same session.
        with open(DOCS_FILE, "w") as f:
            f.write(docs_payload)

# If no files were uploaded this run but a DOCS_FILE exists from earlier
# in this session, reload it so the user doesn't lose their documents
# just because the page reran without a new upload.
if os.path.exists(DOCS_FILE):
    with open(DOCS_FILE, "r") as f:
        documents = json.load(f)

