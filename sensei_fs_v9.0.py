# 🧼 RETRIEVED CONTENT CLEANER
# -------------------------------
def clean_retrieved_content(text):
    """
    Removes duplicate lines from retrieved text.
    PDFs often repeat headers, footers, and slide titles across pages.
    This function keeps the first occurrence of any line and drops repeats.
    Blank lines are always preserved to maintain paragraph structure.
    """
    lines = text.splitlines()
    seen = set()   # Tracks normalised (lowercased, stripped) lines we've already kept
    cleaned = []

    for line in lines:
        # Normalise for comparison: strip whitespace and lowercase.
        # We compare normalised but append the original to preserve formatting.
        normalized = line.strip().lower()

        if normalized == "":
            # Always keep blank lines — they define paragraph boundaries
            cleaned.append(line)
            continue

        if normalized not in seen:
            seen.add(normalized)
            cleaned.append(line)
        # If already seen, silently skip — duplicate line dropped

    return "\n".join(cleaned)

