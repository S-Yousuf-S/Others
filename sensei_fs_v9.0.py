# This is how Streamlit creates the illusion of a persistent conversation —
# the entire history is re-rendered from session_state on every rerun.
for chat in st.session_state.chat:
    with st.chat_message("user"):
        st.markdown(chat["question"])

    with st.chat_message("assistant"):
        st.markdown("📌 **Main Explanation**")
        st.markdown(chat["summary"])
        st.markdown("---")

        # Render each source expander (PDF / Notebook / Slides)
        for src, ans in chat["answers"]:

            label = ""
            if src == "pdf":
                label = "🟥 PDF Source — Retrieved Passages"
            elif src == "notebook":
                label = "🟩 Notebook Source — Code & Notes"
            elif src == "slides":
                label = "🟦 Slides Source — Key Points"

            with st.expander(label, expanded=False):
                st.caption("📎 Relevant excerpts retrieved from your uploaded material.")
                st.divider()

                if src == "notebook":
                    # Notebook content needs special handling for images and code fences
                    render_notebook_content(ans)
                else:
                    # PDF and slides: split into readable paragraph chunks with dividers
                    chunks = format_chunks_for_display(ans, src)
                    for i, chunk in enumerate(chunks):
                        st.markdown(chunk)
                        if i < len(chunks) - 1:
                            st.markdown("---")

