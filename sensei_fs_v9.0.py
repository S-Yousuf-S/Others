# 🤖 GEMINI RESPONSE GENERATOR
# -------------------------------
def generate_summary(question, relevant_sources):
    """
    Sends the retrieved context and user question to Google Gemini
    and returns a structured tutor-style explanation.

    The context is capped at 1000 characters per source to stay within
    safe token limits and keep the prompt focused.
    """
    # Build the context block from all retrieved source chunks
    context = ""
    for _, text in relevant_sources:
        context += text[:1000] + "\n\n"   # Cap per-source context to 1000 chars

    # The prompt instructs Gemini to stay grounded in the provided context
    # and respond as a tutor — clear language, examples, structured output.
    prompt = f"""
    You are Sensei, an expert tutor.

    Provide a clear, concise explanation.
    Use simple language and examples.

    Context:
    {context}

    Question:
    {question}
    """

    # generate_content() sends the prompt to the Gemini model and returns
    # a response object. .text extracts the plain string from it.
    return st.session_state.model.generate_content(prompt).text



